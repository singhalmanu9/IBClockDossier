package essentials;

/**
 * This is the Location class, the class
 * that keeps track of the location of
 * all the elements in the Clock.
 * @author Abhimanyu S.
 *
 */
public class Location 
{
	// instance variables
	public int x;
	public int y;
	
	// constructor
	public Location(int xVal, int yVal)
	{
		x = xVal;
		y = yVal;
	}
}
